"""Shared utilities for SMP02 test scripts."""

import time

import pyvisa

SMP_ADDRESS = "GPIB1::28::INSTR"
SMP_IDN_HINT = "SMP02"

READS_PER_POINT = 3
READ_DELAY = 0.05
POINT_DELAY = 0.15


def connect_smp(verbose=True, address=SMP_ADDRESS, hint=SMP_IDN_HINT):
    """Open a pyvisa session to the SMP02 and return the device handle.

    Retries the *IDN? query up to 10 times to allow the instrument to
    finish its boot sequence. Raises ConnectionError if the identity
    never matches ``hint``.
    """
    rm = pyvisa.ResourceManager()
    if verbose:
        print(f"Connecting to {address}...", end="", flush=True)
    dev = rm.open_resource(address)
    dev.timeout = 5000
    idn = None
    for _ in range(10):
        try:
            idn = dev.query("*IDN?")
        except Exception:
            idn = None
        if idn and hint in idn:
            if verbose:
                print(f"connected to {idn.strip()}")
            return dev
        time.sleep(0.1)
    try:
        dev.close()
    except Exception:
        pass
    raise ConnectionError(
        f"Unexpected identity response {idn!r} from {address}"
    )


def read_tp(dev, tp):
    """Read a diagnostic test point, return averaged value."""
    time.sleep(POINT_DELAY)
    values = []
    for i in range(READS_PER_POINT):
        if i > 0:
            time.sleep(READ_DELAY)
        reply = dev.query(f":DIAG:MEAS:POINT{tp}?")
        values.append(float(reply))
    return sum(values) / len(values)


def query_options(dev):
    """Query *OPT? and return set of installed option names."""
    reply = dev.query("*OPT?").strip()
    return {o for o in reply.split(",") if o != "0"}


def query_hw_modules(dev):
    """Query :DIAG:INFO:MOD? and return dict of module info."""
    reply = dev.query(":DIAG:INFO:MOD?").strip()
    modules = {}
    for entry in reply.split(","):
        parts = entry.strip().split()
        if len(parts) >= 3:
            name = parts[0]
            var = parts[1]
            rev = parts[2]
            modules[name] = (var, rev)
    return modules


def list_hw_modules(dev):
    """Print hardware modules from :DIAG:INFO:MOD?."""
    hw = query_hw_modules(dev)
    for name, (var, rev) in hw.items():
        print(f"  {name:8s}  {var}  {rev}")


def drain_err_queue(dev, max_drain=64):
    """Drain :SYST:ERR? queue, return list of non-"No error" entries."""
    errors = []
    for _ in range(max_drain):
        reply = dev.query(":SYST:ERR?").strip()
        if not reply:
            break
        code = reply.split(",", 1)[0].strip()
        if code in ("0", "+0"):
            break
        errors.append(reply)
    return errors


def query_scalar(dev, cmd, default=""):
    """Query a scalar SCPI value, returning stripped reply or default on error."""
    try:
        return dev.query(cmd).strip()
    except Exception as exc:
        return f"{default} (query failed: {exc})"


# UTILITIES/PROTECT lock levels (band-1 §6.4.1.1, user-manual p.206).
# Passwords are fixed in firmware. Empirically on SMP02 FW 3.70 the
# password is NOT validated over GPIB (any parameter flips STATe to 0)
# and the flag does not gate SCPI access; it only hides front-panel
# menu items. Kept here so unlock_protect() still issues the correct
# pair if/when a firmware actually enforces it.
UNLOCK_PASSWORDS = {
    1: "123456",  # PULSE GEN, YFOM, ALC AMP, LEVEL view menus
    2: "520805",  # REF OSC menu
    3: "490727",  # UTILITIES/DIAG/PARAM menu
}


def unlock_protect(dev, level=1):
    """Clear the UTILITIES/PROTECT lock flag for LEVEL n.

    Sends :SYST:PROT<n>:STAT OFF, <password>. Returns the list of
    error-queue entries generated by the command (empty list = success).
    The manual (p.206) states that a wrong password yields -224
    "Illegal parameter value", but FW 3.70 does not actually enforce
    this — see module docstring note above.
    """
    if level not in UNLOCK_PASSWORDS:
        raise ValueError(f"unsupported lock level {level}")
    drain_err_queue(dev)
    dev.write(f":SYST:PROT{level}:STAT OFF, {UNLOCK_PASSWORDS[level]}")
    return drain_err_queue(dev)


