def detect_a9_variant_scpi(dev):
    """Detect A9 variant via :DIAG:INFO:MOD?.

    Returns (stock, var, rev). stock is None when the VARxx reported by
    the firmware is not yet in A9_SCPI_VAR_MAP.
    """
    hw = query_hw_modules(dev)
    alca = hw.get("ALCA")
    if alca is None:
        return None, None, None
    var, rev = alca
    return A9_SCPI_VAR_MAP.get(var), var, rev


def detect_a9_variant_tp(dev):
    """Detect A9 variant via TP1604 (AM adder, AM off).

    6301: +1.495..+1.505 V ; 6199: +0.995..+1.005 V. The two windows
    are 500 mV apart and both are frequency- and calibration-independent.
    Returns (stock, v1604).
    """
    dev.write("*RST")
    time.sleep(0.5)
    dev.write("SOUR:AM:STAT OFF")
    dev.write("OUTP:STAT OFF")
    time.sleep(0.3)
    v = read_tp(dev, 1604)
    if 1.35 <= v <= 1.65:
        return A9_V_6301, v
    if 0.85 <= v <= 1.15:
        return A9_V_6199, v
    return A9_V_UNKNOWN, v


def detect_a9_variant(dev, verbose=False):
    """Detect A9 variant using SCPI and TP1604, cross-checking the two.

    Returns the stock number string, or A9_V_UNKNOWN if neither source
    yields a definite answer. When both sources disagree, the physical
    measurement wins and a warning is printed if verbose.
    """
    scpi_stock, var, rev = detect_a9_variant_scpi(dev)
    tp_stock, v1604 = detect_a9_variant_tp(dev)

    if verbose:
        print("A9 variant detection:")
        if var is not None:
            mapped = scpi_stock if scpi_stock else "unmapped VAR"
            print(f"  SCPI   ALCA {var} {rev}   -> {mapped}")
        else:
            print("  SCPI   no ALCA entry in :DIAG:INFO:MOD?")
        print(f"  TP1604 (AM off) {v1604:+.3f} V   -> {tp_stock}")

    if scpi_stock and tp_stock != A9_V_UNKNOWN and scpi_stock != tp_stock:
        if verbose:
            print(f"  WARNING: SCPI says {scpi_stock} but TP1604 says {tp_stock};"
                  f" trusting TP1604")
        return tp_stock
    if scpi_stock:
        return scpi_stock
    return tp_stock
